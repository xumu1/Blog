import java.util.List;

/**
 * Definition for singly-linked list.
 * public class ListNode {
 * int val;
 * ListNode next;
 * ListNode() {}
 * ListNode(int val) { this.val = val; }
 * ListNode(int val, ListNode next) { this.val = val; this.next = next; }
 * }
 */
class Solution {
    public void reorderList(ListNode head) {
        int len = 0;
        ListNode tmp = head;
        while (tmp != null) {
            len++;
            tmp = tmp.next;
        }
        if (len <= 2) {
            return;
        }
        int mid = (len + 1) / 2;
        ListNode midNode = head;
        for (int i = 1; i < mid; i++) {
            midNode = midNode.next;
        }
        ListNode one = head;
        ListNode two = reverseList(midNode.next);
        midNode.next = null;
        bind(one, two);
    }

    public void bind(ListNode one, ListNode two) {
        ListNode oneNext;
        ListNode twoNext;
        while (two != null) {
            oneNext = one.next;
            twoNext = two.next;
            one.next = two;
            two.next = oneNext;
            one = oneNext;
            two = twoNext;
        }
    }

    public ListNode reverseList(ListNode root) {
        ListNode pre = null;
        ListNode cur = root;
        while (cur != null) {
            ListNode next = cur.next;
            cur.next = pre;
            pre = cur;
            cur = next;
        }
        return pre;
    }
}
