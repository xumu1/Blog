/**
 * Definition for a binary tree node.
 * public class TreeNode {
 * int val;
 * TreeNode left;
 * TreeNode right;
 * TreeNode() {}
 * TreeNode(int val) { this.val = val; }
 * TreeNode(int val, TreeNode left, TreeNode right) {
 * this.val = val;
 * this.left = left;
 * this.right = right;
 * }
 * }
 */
class Solution {
    public int sumNumbers(TreeNode root) {
        int[] res = new int[]{0};
        fun(root, res, 0);
        return res[0];
    }

    public void fun(TreeNode root, int[] res, int chain) {
        if (root == null) {
            return;
        }
        if (root.left == null && root.right == null) {
            res[0] = res[0] + chain * 10 + root.val;
            return;
        }
        fun(root.left, res, chain * 10 + root.val);
        fun(root.right, res, chain * 10 + root.val);
    }
}
