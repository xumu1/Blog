class Solution {
    public boolean canJump(int[] nums) {
        // ä½¿ç”¨ dp
        int len = nums.length;
        if (len == 1) {
            return true;
        }
        // ä»£è¡¨å«ä¹‰ï¼šæ˜¯å¦èƒ½åˆ°è¾¾ç»ˆç‚¹
        boolean[] bool = new boolean[len];
        for (int i = len - 1; i >= 0; i--) {
            int end = nums[i] + i;
            if (end >= len - 1 || bool[end]) {
                bool[i] = true;
            } else {
                int tmp = end - 1;
                while (tmp > i) {
                    if (bool[tmp]) {
                        bool[i] = true;
                        break;
                    }
                    tmp--;
                }
            }
        }
        return bool[0];
    }
}
